import os

def unformat():
    for filename in os.listdir('.'):
        if os.path.isfile(filename):
            try:
                new_name=filename.replace("_","")
                print("new name:",new_name)
                os.rename(filename, new_name)
            except Exception as e:
                pass

unformat()
